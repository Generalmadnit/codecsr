class Student{
    String name;
    String email;
    int id;
    public void setName(String n){
        name = n;
    }
    public void setEmail(String e){
        email = e;
    }
    public void setId(int i){
        id = i;
    }
    public String getName(){
        return name;
    }
    public String getEmail(){
        return email;
    }
    public int setId(){
        return id;
    }
}